private void deleteSubjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteSubjectActionPerformed
// TODO add your handling code here:
    try {
        int row = subjectTable.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please Select Record");
        } else {
            Object id = subjectTable.getModel().getValueAt(row, 0);
            if (id.toString().isEmpty()) {
            } else {
                
                int deleteResponse = JOptionPane.showInternalConfirmDialog(this, "Are You sure Want to DELETE\n the Selected Record?");
                if (deleteResponse == 0) {
                    boolean deleted = new Controller().deleteRecord(id.toString(), "Subject", "SubjectId");
                    if (deleted == true) {
                        JOptionPane.showMessageDialog(null, "Record Deleted Successfully");
                        subjectTable.setModel(new Controller().subjectTableModel());
                    } else {
                        JOptionPane.showMessageDialog(null, "ERROR\nRecord\n NOT Deleted Successfully.. Please try again");
                    }
                }
            }
        }
    } catch (Exception ex) {
        System.out.println(ex.getMessage());
    }
}//GEN-LAST:event_deleteSubjectActionPerformed

