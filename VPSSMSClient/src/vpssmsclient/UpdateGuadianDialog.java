private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
// TODO add your handling code here:
    try {
        if (firstName.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter First Name");
        } else if (lastName.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter  Last Name");
        } else if (middleName.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter  Middle Name");
        } else if (residence.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter  Place of Residence");
        } else if (occupation.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter  Occupation");
        } else {
            if (contact.getText().trim().isEmpty()) {
                contact.setText("none");
            }
            boolean guardianRegistered = new Controller().updateGuardian(new Guardian(
                    studentId,
                    this.guardian.getGuardianId(),
                    firstName.getText(),
                    lastName.getText(),
                    middleName.getText(),
                    residence.getText(),
                    occupation.getText(),
                    gender.getSelectedItem().toString(),
                    contact.getText(),
                    status.getSelectedItem().toString()));
            if (guardianRegistered == true) {
                JOptionPane.showMessageDialog(this, "Guardian Updated Successfully");
                StudentRegistrationWindow.guardianTable.setModel(new Controller().GuardianTableModel());
            } else {
                JOptionPane.showMessageDialog(this, "ERROR\n Guardian \nNOT \nUpdated \n Please Try again");
            }

        }


    } catch (Exception ex) {
        System.out.println(ex.getMessage());
    }
}//GEN-LAST:event_saveActionPerformed

